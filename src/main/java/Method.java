public class Method {
    public static void main(String[] args) {
        System.out.println(sum(2, 5, 8));
        System.out.println(multiply(6,9));
    }

    public static int sum(int a, int b, int c) {
        return a + b + c;
    }

    public static int multiply(int a, int b) {
        return a * b;
    }
}
